    private static getDetailData(date): any {
        for (let i = 0; i < CalendarData.canlendar.length; i++) {
            if (CalendarData.canlendar[i].dateTime === date) {
                // console.log("row: ", row);
                // 获取日期，计算星期几
                for (let j = 0; j < CalendarData.canlendar.length; j++) {
                    const week = CalendarData.toDateFromString(date);
                    return {
                        dateTime: CalendarData.canlendar[((CalendarData.canlendar[i].extended + 1 + i))] && CalendarData.canlendar[((CalendarData.canlendar[i].extended + 1 + i))].dateTime.toString(),
                        isWorking: CalendarData.canlendar[i].isWorking,
                        week: week.getDay()
                    };
                }
            }
        }
    }

    // 将20101010这种格式转为日期格式
    static toDateFromString(strDate: string): any {
        if (strDate.length !== 8) {
            return;
        }
        let newDate = null;
        const nYear = parseInt(strDate.substring(0, 4), 10);
        const nMonth = parseInt(strDate.substring(4, 6), 10);
        const nDay = parseInt(strDate.substring(6, 8), 10);
        if (isNaN(nYear) === true || isNaN(nMonth) === true || isNaN(nDay) === true) {
            return;
        }
        newDate = new Date(nYear, nMonth - 1, nDay);
        return newDate;
    }

